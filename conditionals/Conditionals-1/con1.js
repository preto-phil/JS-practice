//// If, Else, Else If

let greeting;

// If Statements

if (hour < 18) {
    greeting = "Good Day!";
}

// Else Statement

if (hour < 18) {
    greeting = "Good Day!"
} else {
    greeting = "Good Evening!"
}

// Else If Statement

if (hour < 10) {
    greeting = "Good Morning!"
} else if (hour < 18) {
    greeting = "Good Day!"
} else {
    greeting = "Good Evening!"
}
